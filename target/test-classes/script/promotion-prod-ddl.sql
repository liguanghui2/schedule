DROP TABLE PROMOTIONV2 IF EXISTS;
CREATE TABLE PROMOTIONV2
(
  ID         BIGINT GENERATED BY DEFAULT AS IDENTITY (
  START WITH 1 ) NOT NULL,
  START_DATE DATE,
  END_DATE   DATE,
  TYPE       INTEGER,
  MC_SITE_ID INTEGER,
  IS_DELETE  INTEGER,
  IS_TRIAL   INTEGER,
  IS_PERIOD  INTEGER,
  PRIMARY KEY (ID)
);

DROP TABLE PROMOTIONV2_LANDING_PRODUCT IF EXISTS;
CREATE TABLE PROMOTIONV2_LANDING_PRODUCT
(
  ID                  BIGINT GENERATED BY DEFAULT AS IDENTITY (
  START WITH 1 ) NOT NULL,
  PROMOTION_ID        BIGINT DEFAULT NULL,
  PRODUCT_ID          BIGINT DEFAULT NULL,
  NORMAL_PRICE        DECIMAL(11, 2),
  ACTIVITY_PRICE      DECIMAL(11, 2),
  TOTAL_QUANTITY_TYPE INTEGER,
  TOTAL_QUANTITY      INTEGER,
  SOLD_DATE           DATE,
  SOLD_NUM            INTEGER,
  ACTIVITY_POINT      INTEGER,
  IS_EFFECTIVE        INTEGER,
  PM_INFO_ID          INTEGER,
  MERCHANT_ID         INTEGER,
  PRIMARY KEY (ID)
);

DROP TABLE PROMOTIONV2_MERCHANT IF EXISTS;
CREATE TABLE PROMOTIONV2_MERCHANT
(
  ID           BIGINT GENERATED BY DEFAULT AS IDENTITY (
  START WITH 1 ) NOT NULL,
  PROMOTION_ID BIGINT DEFAULT NULL,
  MERCHANT_ID  BIGINT DEFAULT NULL,
  PRIMARY KEY (ID)
);

DROP TABLE PROMOTIONV2_PERIOD_TIME IF EXISTS;
CREATE TABLE PROMOTIONV2_PERIOD_TIME
(
  ID           BIGINT GENERATED BY DEFAULT AS IDENTITY (
  START WITH 1 ) NOT NULL,
  PROMOTIONV2_ID BIGINT DEFAULT NULL,
  START_DATE DATE,
  END_DATE   DATE,
  IS_DELETE  INTEGER,
  PRIMARY KEY (ID)
);
DROP TABLE BS_PM_STOCK IF EXISTS;
CREATE TABLE BS_PM_STOCK (
  id                    BIGINT GENERATED BY DEFAULT AS IDENTITY (
  START WITH 1 ) NOT NULL,
  provinceId            BIGINT DEFAULT NULL,
  cityId                BIGINT DEFAULT NULL,
  countyId              BIGINT DEFAULT NULL,
  areaId                BIGINT DEFAULT NULL,
  pmInfoId              BIGINT DEFAULT NULL,
  merchantId            BIGINT DEFAULT NULL,
  availableStock        BIGINT DEFAULT NULL,
  localAvailableStock   BIGINT DEFAULT NULL,
  sharedAvailableStock  BIGINT DEFAULT NULL,
  ts                    BIGINT DEFAULT NULL,
  tsf                   BIGINT DEFAULT NULL,
  vs                    BIGINT DEFAULT NULL,
  vsf                   BIGINT DEFAULT NULL,
  isSupportVirtualStock TINYINT DEFAULT '0',
  canSale               TINYINT DEFAULT '0',
  canShow               TINYINT DEFAULT '0',
  saleType              TINYINT DEFAULT '0',
  PRIMARY KEY (ID)
);

DROP TABLE BS_ACTIVITY_STOCK IF EXISTS;
CREATE TABLE BS_ACTIVITY_STOCK (
  id             BIGINT GENERATED BY DEFAULT AS IDENTITY (
  START WITH 1 ) NOT NULL,
  pmInfoId       BIGINT DEFAULT NULL,
  ruleId         BIGINT DEFAULT NULL,
  availableStock BIGINT DEFAULT NULL,
  activityType	 int default 3,
  PRIMARY KEY (ID)
);